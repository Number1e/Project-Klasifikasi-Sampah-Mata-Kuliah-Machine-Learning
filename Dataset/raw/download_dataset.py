import gdown
import os

def download_drive_folder():
    # 1. Masukkan ID FOLDER (Bukan ID File)
    # Contoh URL: drive.google.com/drive/folders/16TLvMp39...
    folder_id = '16TLvMp39vcos9LCal64AFJW0DOLDfc07'

    # 2. Tentukan mau ditaruh di folder mana hasilnya
    output_directory = 'data/raw/dataset_folder'

    # Buat direktori tujuan jika belum ada
    if not os.path.exists(output_directory):
        os.makedirs(output_directory)

    print(f"Mulai mendownload folder ID: {folder_id} ke {output_directory}...")

    # 3. Eksekusi Download Folder
    # Perhatikan: menggunakan 'download_folder', bukan 'download'
    try:
        gdown.download_folder(id=folder_id, output=output_directory, quiet=False, use_cookies=False)
        print("\nDownload Selesai!")
        print(f"Cek folder Anda di: {output_directory}")

    except Exception as e:
        print(f"\nTerjadi kesalahan: {e}")
        print("Saran: Pastikan Permission folder di Google Drive adalah 'Anyone with the link'.")

if __name__ == "__main__":
    download_drive_folder()